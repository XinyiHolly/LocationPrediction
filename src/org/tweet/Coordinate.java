package org.tweet;

public class Coordinate {
	public long id;
	public double lon = 0.0 ;
	public double lat = 0.0 ;
	
	public Coordinate(double x, double y){
		this.lon = x;
		this.lat = y;
	}	
	
	public void setX(double x){
		this.lon = x;
	}
	public void setY(double y){
		this.lat = y;
	}
	public double getX(){
		return this.lon;
	}
	public double getY(){
		return this.lat;
	}
	
}
